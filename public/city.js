function populateStates() {
    var stateDropdown = document.getElementById("state");
    for (var state in stateCityData) {
        var option = document.createElement("option");
        option.value = state;
        option.text = state;
        stateDropdown.appendChild(option);
    }
}

// Function to populate the city dropdown based on the selected state
function populateCities() {
    var stateDropdown = document.getElementById("state");
    var cityDropdown = document.getElementById("city");
    var selectedState = stateDropdown.value;

    // Clear the city dropdown
    cityDropdown.innerHTML = "";

    // Populate the city dropdown with cities from the selected state
    for (var i = 0; i < stateCityData[selectedState].length; i++) {
        var option = document.createElement("option");
        option.value = stateCityData[selectedState][i];
        option.text = stateCityData[selectedState][i];
        cityDropdown.appendChild(option);
    }
}


window.onload = populateStates;